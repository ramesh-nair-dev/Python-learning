def sayhi():
    print("Hello, User")

# This code defines a function named sayhi that prints a greeting message.
# sayhi():
# This line calls the sayhi function, which executes the code inside it.

sayhi()
# This code demonstrates how to define and call a function in Python.

# We can also pass parameters to functions to make them more flexible.

def greet(name):
    print(f"Hello, {name}!")

# This code defines a function named greet that takes a parameter name and prints a personalized greeting message.

greet("Alice")
# This code calls the greet function with the argument "Alice", which will print "Hello, Alice!".

